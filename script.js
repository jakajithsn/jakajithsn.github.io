const header = document.querySelector(".site-header");
const menuToggle = document.querySelector(".menu-toggle");
const navPanel = document.querySelector(".nav-panel");
const translatable = document.querySelectorAll("[data-i18n]");
const revealItems = document.querySelectorAll(".reveal");
const lightbox = document.querySelector(".lightbox");
const lightboxImage = document.querySelector(".lightbox img");
const lightboxClose = document.querySelector(".lightbox-close");

const renderBilingualContent = () => {
  document.documentElement.lang = "en";
  translatable.forEach((item) => {
    const key = item.dataset.i18n;
    const tamil = translations.ta[key];
    const english = translations.en[key];

    if (tamil && english) {
      if (tamil === english) {
        item.textContent = tamil;
        return;
      }

      item.replaceChildren();
      const tamilText = document.createElement("span");
      const englishText = document.createElement("span");
      tamilText.className = "ta-text";
      englishText.className = "en-text";
      tamilText.textContent = tamil;
      englishText.textContent = english;
      item.append(englishText, tamilText);
    }
  });
};

const toggleHeader = () => {
  header.classList.toggle("scrolled", window.scrollY > 20);
};

menuToggle.addEventListener("click", () => {
  const isOpen = navPanel.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

navPanel.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    navPanel.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

document.querySelectorAll(".gallery-item").forEach((item) => {
  item.addEventListener("click", () => {
    const image = item.querySelector("img");
    lightboxImage.src = image.src;
    lightboxImage.alt = image.alt;
    lightbox.classList.add("open");
    lightbox.setAttribute("aria-hidden", "false");
  });
});

const closeLightbox = () => {
  lightbox.classList.remove("open");
  lightbox.setAttribute("aria-hidden", "true");
  lightboxImage.src = "";
};

lightboxClose.addEventListener("click", closeLightbox);
lightbox.addEventListener("click", (event) => {
  if (event.target === lightbox) closeLightbox();
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox.classList.contains("open")) {
    closeLightbox();
  }
});

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16 }
);

revealItems.forEach((item) => observer.observe(item));
window.addEventListener("scroll", toggleHeader, { passive: true });

toggleHeader();
renderBilingualContent();
